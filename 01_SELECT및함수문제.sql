--17
SELECT EMP_NAME,
FLOOR(ABS(HIRE_DATE - SYSDATE)) AS "근무일수1",
FLOOR(ABS(SYSDATE - HIRE_DATE)) AS "근무일수2"
FROM EMPLOYEE;

